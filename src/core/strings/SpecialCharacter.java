package core.strings;

public class SpecialCharacter {
    public static void main(String[] args) {
        // Single quote
        System.out.println("Hello! My name is \'Tom Tran\'.");

        // Double quote
        System.out.println("Hello! My name is \"Tom Tran\".");

        // Backslash
        System.out.println("Hello! My name is \\Tom Tran\\.");

        // Newline
        System.out.println("Hello!\nMy name is Tom Tran.");

        // Carriage return
        System.out.println("Hello!\rMy name is Tom Tran.");

        // Tab
        System.out.println("Hello!\tMy name is Tom Tran.");

        // Backspace
        System.out.println("Hello!\bMy name is Tom Tran.");

        // Form feed
        System.out.println("Hello!\fMy name is Tom Tran.");
    }
}
